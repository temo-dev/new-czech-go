// ── Rate limiter ─────────────────────────────────────────────────────────────

const aiImageRateLimit = 5 // max requests per minute per admin

type rateWindow struct {
	count     int
	windowEnd time.Time
}

type aiImageRateLimiter struct {
	mu      sync.Mutex
	windows map[string]rateWindow
}

func newAiImageRateLimiter() *aiImageRateLimiter {
	return &aiImageRateLimiter{windows: make(map[string]rateWindow)}
}

// allow returns true if the admin may make another request this minute.
func (rl *aiImageRateLimiter) allow(email string) bool {
	rl.mu.Lock()
	defer rl.mu.Unlock()

	now := time.Now()
	w := rl.windows[email]
	if now.After(w.windowEnd) {
		// New or expired window — reset.
		rl.windows[email] = rateWindow{count: 1, windowEnd: now.Add(time.Minute)}
		return true
	}
	if w.count >= aiImageRateLimit {
		return false
	}
	w.count++
	rl.windows[email] = w
	return true
}

// replicateBaseURL is the base URL for Replicate API calls. Overrideable in tests.
var replicateBaseURL = "https://api.replicate.com"

